package com.example.bibliotheque.Object;

public class ObjectStatus {
    int Id;
    String Name;
    public ObjectStatus(){
    }

    public int getId() {
        return Id;
    }

    public void setId(int id) {
        Id = id;
    }

    public String getName() {
        return Name;
    }

    public void setName(String name) {
        Name = name;
    }
}
